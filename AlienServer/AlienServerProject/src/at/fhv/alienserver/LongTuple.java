public class LongTuple<TypeA, TypeB, TypeC, TypeD> {
    public TypeA a;
    public TypeB b;
    public TypeC c;
    public TypeD d;

    public LongTuple(TypeA a, TypeB b, TypeC c, TypeD d){
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }
}

